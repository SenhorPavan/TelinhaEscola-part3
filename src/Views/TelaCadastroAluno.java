    private void BotaoCadastrarAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoCadastrarAlunoActionPerformed
        // TODO add your handling code here:
        
        String nome = CampoNomeAluno.getText();
        String cpf = CampoCpfAluno.getText();
        String endereco = CampoEnderecoAluno.getText();
        String matricula = CampoMatriculaAluno.getText();
        
        Aluno c = new Aluno(matricula,nome,cpf,endereco);
        
        escolaS.EscolaS.ListaDePessoas.add(c);
        
        JOptionPane.showMessageDialog(rootPane, "O CADASTRO FOI REALIZADO COM SUCESSO");

        CampoNomeAluno.setText("");
        CampoEnderecoAluno.setText("");
        CampoMatriculaAluno.setText("");
        CampoCpfAluno.setText("");
        
        
        
    }//GEN-LAST:event_BotaoCadastrarAlunoActionPerformed

